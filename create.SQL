CREATE TABLE TB_TIPO_VEICULO (
  ID_TIPO_VEICULO int PRIMARY KEY AUTO_INCREMENT,
  DESCRICAO_TIPO_VEICULO varchar(255),
  UTILIZACAO varchar(255)
);

CREATE TABLE TB_VEICULO (
  ID_VEICULO int PRIMARY KEY AUTO_INCREMENT,
  ID_TIPO_VEICULO int REFERENCES TB_TIPO_VEICULO(ID_TIPO_VEICULO),
  PLACA varchar(50),
  COR varchar(50)
);

CREATE TABLE TB_TIPO_PRODUTO_MEDICO (
  ID_TIPO_PRODUTO_MEDICO int PRIMARY KEY AUTO_INCREMENT,
  DESCRICAO_TIPO_PRODUTO_MEDICO varchar(255),
  CATEGORIA varchar(50)
);

CREATE TABLE TB_PRODUTO_MEDICO (
  ID_PRODUTO_MEDICO int PRIMARY KEY AUTO_INCREMENT,
  NOME_PRODUTO varchar(255),
  DESCRICAO varchar(255),
  ID_TIPO_PRODUTO_MEDICO int REFERENCES TB_TIPO_PRODUTO_MEDICO(ID_TIPO_PRODUTO_MEDICO),
  MARCA varchar(50)
);

CREATE TABLE TB_PACIENTE (
  ID_PACIENTE int PRIMARY KEY AUTO_INCREMENT,
  NOME_PACIENTE varchar(255),
  ENDERECO varchar(255),
  DATA_NASCIMENTO date,
  TELEFONE varchar(20)
);

CREATE TABLE TB_UNIDADE_SAUDE (
  ID_UNIDADE_SAUDE int PRIMARY KEY AUTO_INCREMENT,
  NOME_UNIDADE varchar(255),
  ENDERECO varchar(255),
  TIPO_UNIDADE varchar(50)
);

CREATE TABLE TB_TRANSPORTE (
  ID_TRANSPORTE int PRIMARY KEY AUTO_INCREMENT,
  ID_VEICULO int REFERENCES TB_VEICULO(ID_VEICULO),
  DATA_HORA_INICIO datetime,
  DATA_HORA_FIM datetime,
  ORIGEM varchar(255),
  DESTINO varchar(255)
);

CREATE TABLE TB_VISITA (
  ID_VISITA int PRIMARY KEY AUTO_INCREMENT,
  ID_PACIENTE int REFERENCES TB_PACIENTE(ID_PACIENTE),
  ID_UNIDADE_SAUDE int REFERENCES TB_UNIDADE_SAUDE(ID_UNIDADE_SAUDE),
  DATA_VISITA date,
  MOTIVO_VISITA varchar(255)
);

CREATE TABLE TB_ESTOQUE_PRODUTO_MEDICO (
  ID_ESTOQUE_PRODUTO_MEDICO int PRIMARY KEY AUTO_INCREMENT,
  ID_PRODUTO_MEDICO int REFERENCES TB_PRODUTO_MEDICO(ID_PRODUTO_MEDICO),
  QUANTIDADE_DISPONIVEL int,
  DATA_ULTIMA_ENTRADA datetime,
  DATA_ULTIMA_SAIDA datetime
);

CREATE TABLE TB_PACIENTE_TRANSPORTE (
  ID_PACIENTE int REFERENCES TB_PACIENTE(ID_PACIENTE),
  ID_TRANSPORTE int REFERENCES TB_TRANSPORTE(ID_TRANSPORTE),
  MOTIVO_TRANSPORTE varchar(255),
  OBSERVACOES varchar(255),
  PRIMARY KEY (ID_PACIENTE, ID_TRANSPORTE)
);

CREATE TABLE TB_PRODUTO_MEDICO_TRANSPORTE (
  ID_PRODUTO_MEDICO int REFERENCES TB_PRODUTO_MEDICO(ID_PRODUTO_MEDICO),
  ID_TRANSPORTE int REFERENCES TB_TRANSPORTE(ID_TRANSPORTE),
  QUANTIDADE int,
  OBSERVACOES varchar(255),
  PRIMARY KEY (ID_PRODUTO_MEDICO, ID_TRANSPORTE)
);

CREATE TABLE TB_VISITA_PRODUTO_MEDICO (
  ID_PRODUTO_MEDICO int REFERENCES TB_PRODUTO_MEDICO(ID_PRODUTO_MEDICO),
  ID_VISITA int REFERENCES TB_VISITA(ID_VISITA),
  QUANTIDADE int,
  NUMERO_RECEITA int,
  PRIMARY KEY (ID_PRODUTO_MEDICO, ID_VISITA)
);